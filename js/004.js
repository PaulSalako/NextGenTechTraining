// This Create a box to store that it's weekend (set to true meaning yes)
const WeekEndTrue = true;
// This Show a question asking if it's weekend and display the answer (true)
console.log("Is today weekend? " + WeekEndTrue);

// This Create another box to store that it's not weekend (set to false meaning no)
const WeekEndFalse = false;
// This Show the same question but now display the new answer (false)
