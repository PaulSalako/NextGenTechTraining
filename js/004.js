// Store the temperature in Celsius (30 degrees)
const celsiusTemp = 30;
// Convert Celsius to Fahrenheit using the formula and store the result
// The formula is: multiply by 9/5 and add 32
const fahrenheitTemp = (celsiusTemp * 9/5) + 32;

// This Show the temperature in Celsius
console.log("Temperature in Celsius: " + celsiusTemp+"C" );
// This Show the converted temperature in Fahrenheit
console.log("Temperature in Fahrenheit: " + fahrenheitTemp+"C");